-- Добавляет колонку `status` в таблицу `registrations`, чтобы фиксировать статус игры на момент заявки команды.
-- Выполните этот SQL в phpMyAdmin (или другом клиенте), чтобы сохранить текущие данные.

ALTER TABLE `registrations`
    ADD COLUMN `status` TINYINT(1) NOT NULL DEFAULT 1 COMMENT 'Статус игры на момент регистрации: 1 — есть места, 2 — резерв, 3 — регистрация закрыта' AFTER `game_id`;

-- После выполнения запроса все существующие регистрации получат статус 1 (есть места).
-- Если колонка уже существует, MySQL сообщит об ошибке — в этом случае дополнительных действий не требуется.

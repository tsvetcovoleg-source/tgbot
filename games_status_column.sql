-- Добавляет колонку `status` в таблицу `games`, чтобы управлять доступностью игр в списках.
-- Выполните этот SQL в phpMyAdmin (или другом клиенте), чтобы сохранить текущие данные.

ALTER TABLE `games`
    ADD COLUMN `status` TINYINT(1) NOT NULL DEFAULT 1 COMMENT '1 — есть места, 2 — только резерв, 3 — регистрация закрыта' AFTER `type`;

-- После выполнения запроса все существующие игры получат статус 1 (есть места).
-- Если колонка уже существует, MySQL сообщит об ошибке — в этом случае дополнительных действий не требуется.

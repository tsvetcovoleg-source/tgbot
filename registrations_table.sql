-- Выполните этот SQL в phpMyAdmin (или другой клиент), чтобы сохранить текущие данные.

ALTER TABLE `registrations`
    MODIFY COLUMN `quantity` VARCHAR(50) NULL DEFAULT NULL COMMENT 'Размер команды или значение «Пока не знаем»';

-- Исправляем прошлые выборы «Пока не знаем», которые могли сохраниться как 0.
UPDATE `registrations`
    SET `quantity` = 'Пока не знаем'
    WHERE `quantity` = '0' OR `quantity` = 0;

